package eu.oberon.oss.util.test.raw;

/**
 * Describes (for text data) the character case to be used.
 *
 * @author TigerLilly64
 * @since 1.0.0
 */
public enum CaseType {
    /**
     * Generates upper case characters
     *
     * @since 1.0.0
     */
    UPPER,
    /**
     * Generates lower case characters
     *
     * @since 1.0.0
     */
    LOWER,

    /**
     * Generates randomly upper or lowercase characters.
     *
     * @since 1.0.0
     */
    RANDOM;
}
